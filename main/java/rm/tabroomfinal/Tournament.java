package rm.tabroomfinal;

public class Tournament {

    private String tornamentDate;
    private String locale;
    private String torunamentName;
    private String city;
    private String registration;
    private String tournamentLink;

    public String getTornamentDate() {
        return tornamentDate;
    }

    public void setTornamentDate(String tornamentDate) {
        this.tornamentDate = tornamentDate;
    }

    public String getLocale() {
        return locale;
    }

    public void setLocale(String locale) {
        this.locale = locale;
    }

    public String getTorunamentName() {
        return torunamentName;
    }

    public void setTorunamentName(String torunamentName) {
        this.torunamentName = torunamentName;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getRegistration() {
        return registration;
    }

    public void setRegistration(String registration) {
        this.registration = registration;
    }

    public String getTournamentLink() {
        return tournamentLink;
    }

    public void setTournamentLink(String tournamentLink) {
        this.tournamentLink = tournamentLink;
    }
}
